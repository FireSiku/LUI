-- Movement Speed Infotext

-- ####################################################################################################################
-- ##### Setup and Locals #############################################################################################
-- ####################################################################################################################

---@class LUIAddon
local LUI = select(2, ...)
local L = LUI.L

---@class LUI.Infotext
local module = LUI:GetModule("Infotext")
local element = module:NewElement("MoveSpeed", "AceEvent-3.0")

-- ####################################################################################################################
-- ##### Module Functions #############################################################################################
-- ####################################################################################################################

function element:SetMoveSpeed()
	local baseSpeed = BASE_MOVEMENT_SPEED
	local speed, runSpeed = GetUnitSpeed("player")
	if speed == 0 then
		speed = runSpeed
	end

	element.text = format("Speed: %d", speed)
	element:UpdateTooltip()
end

function element.OnTooltipShow(GameTooltip)
	element:TooltipHeader(_G.STAT_MOVEMENT_SPEED)
	element:AddHint("Speed is shown in yards per second.\nA value of 7 is 100% movement speed, 14 is 200%, etc.")
end

-- ####################################################################################################################
-- ##### Infotext Display #############################################################################################
-- ####################################################################################################################

-- ####################################################################################################################
-- ##### Framework Events #############################################################################################
-- ####################################################################################################################

function element:OnCreate()
	-- module:SetMoveSpeed()
	element:AddUpdate("SetMoveSpeed", 1)
end
